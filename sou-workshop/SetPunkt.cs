class SetPunkt {
    public int DayValue;
    public int NightValue;

    public SetPunkt (int DayValue, int NightValue) {
        this.DayValue = DayValue;
        this.NightValue = DayValue;
    }

    public int GetDayValue () {
        return DayValue;
    }

    public int GetNightValue () {
        return NightValue;
    }
}